
# Max function
def my_max(num1, num2):
    print('-- FUNCTIONS2 -- MAX number between', num1, 'and', num2)
    if num1 > num2:
        return num1
    else:
        return num2


# Min function
def my_min(num1, num2):
    print('-- FUNCTIONS2 -- MIN number between', num1, 'and', num2)
    if num1 > num2:
        return num2
    else:
        return num1

